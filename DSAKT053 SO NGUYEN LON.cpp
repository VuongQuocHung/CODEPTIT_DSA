int dp[1005][1005];
void solve(string s1, string s2){
	int n1 = s1.size(), n2 = s2.size();
	for(int i = 1; i <= n1; i++){
		for(int j = 1; j <= n2; j++){
			if(s1[i - 1] == s2[j - 1]) dp[i][j] = dp[i - 1][j - 1] + 1;
			else dp[i][j] = max(dp[i][j - 1], dp[i - 1][j]);
		}
	}
	cout << dp[n1][n2] << endl;
}

